#!/usr/bin/env python3
import fileinput
from collections import deque

def iterbatch(iterable, size=1):
    cache = deque(maxlen=size)

    for item in iterable:
        if len(cache) == size:
            yield tuple(cache)

        cache.append(item)


seq = [0]
seq.extend(sorted(int(line) for line in fileinput.input()))
seq.append(seq[-1] + 3)

print(2 ** sum(1 for a, b, c in iterbatch(seq, 3) if c - a < 3))

